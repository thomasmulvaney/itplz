def run_moltypes(directories=[], files=[]):
    mdls = itplz.mol_def.MolDefLoader()
    for d in directories:
        mdls.load_directory(d)
    for f in files:
        mdls.load_itp_file(f)

    for ds in mdls.mol_defs.values():
        for d in ds:
            print(f"{d.name:<10}{d.file}:{d.line_num}")
    


def create_includes(mol_defs, molecules):
    """Given a list of molecules, generate the include statement for them."""
    def_to_molecules = defaultdict(list)
    for mol in molecules:
        def_to_molecules[mol_defs[mol][0].file].append(mol)

    for itp in def_to_molecules:
        print(f'#include "{itp}" ; {",".join(def_to_molecules[itp])}')



def run_print_includes(directories=[], files=[], moltypes=[]):
    mdls = itplz.mol_def.MolDefLoader()
    for d in directories:
        mdls.load_directory(d)
    for f in files:
        mdls.load_itp_file(f)

    print("; Inlcudes generated by itplz")
    create_includes(mdls.mol_defs, moltypes)


