def run_add_includes(top, directories=[], files=[]):
    topology = itplz.topology.Topology(top)
    mdls = itplz.mol_def.MolDefLoader()
    for d in directories:
        mdls.load_directory(d)
    for f in files:
        mdls.load_itp_file(f)
    last_include =  max(topology.mol_defs.include_lines.values())
    with open(top, 'r') as f:
        for idx, line in enumerate(f.readlines()):
            print(line[:-1])
            if idx == last_include:
                print('; added by itplz')
                create_includes(mdls.mol_defs, [f for f in topology.moleculetypes if not topology.mol_defs.contains(f)])
                

    

def run_check_top(top, extra_directories=[], extra_files=[]):
    topology = itplz.topology.Topology(top)
    extra_mdls = itplz.mol_def.MolDefLoader()
    for d in extra_directories:
        extra_mdls.load_directory(d)
    for f in extra_files:
        extra_mdls.load_itp_file(f)
    not_in_top = []
    not_in_extras = []
    print("✔  Molecule types which have moleculartype definitions already")
    for mt in topology.moleculetypes:
        if topology.mol_defs.contains(mt):
            include = topology.mol_defs.get(mt)
            print(f"{mt:<10} {include.file}:{include.line_num}")
        else:
            not_in_top.append(mt)
    print()
    print("# Molecules which have types defined in extra itp files")
    for mt in not_in_top:
        if extra_mdls.get(mt) is not None:
            include = extra_mdls.get(mt)
            print(f"{mt:<10} {include.file}:{include.line_num}")
        else:
            not_in_extras.append(mt)
    print()
    print("# Molecules which lack a definition")
    for mt in not_in_extras:
        print(f"{mt}") 

    print()
    print("# Includes which could not be loaded")
    for inc in topology.mol_defs.failed_includes:
        print(f"{inc}")

    print("# Molecules with multiple definitions")
    for mt in topology.moleculetypes:
        if extra_mdls.contains(mt):
            if len(extra_mdls.mol_defs[mt]) > 1:
                print(f"{mt} is defined in:")
                for f in extra_mdls.mol_defs[mt]:
                    print(f"  {f.file}:{f.line_num}")



def main():
    args = parser.parse_args()
    if args.sub_name == "moltypes":
        run_moltypes(args.directories, args.files)

    if args.sub_name == "print-includes":
        run_print_includes(args.directories, args.files, args.moltypes)

    if args.sub_name == "check-top":
        run_check_top(args.top, args.directories, args.files)

    if args.sub_name == "add-includes":
        run_add_includes(args.top, args.directories, args.files)
